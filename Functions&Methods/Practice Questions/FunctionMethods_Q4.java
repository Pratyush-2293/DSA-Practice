public class FunctionMethods_Q4 {
    public static void main(String args[]){
        int a = 5;
        int b = 8;

        System.out.println("Minimum number is: "+ Math.min(a, b));
        System.out.println("Maximum number is: "+ Math.max(a, b));
        System.out.println("Square Root of 25 is: "+Math.sqrt(25));
        System.out.println("a raised to power b is: "+Math.pow(a, b));
        System.out.println("Math.avg does not exist");
        System.out.println("Absolute value of -25 is: "+Math.abs(-25));
    }
}
